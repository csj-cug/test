function Ainv = inv_4th_Order_Sym(A)
%A should be symmetric
N_Slice = size(A,3);
Ainv = zeros(size(A));
% DetA_vector = zeros(N_Slice,1);

Ainv(1,1,:) = A(2,2,:).*A(3,3,:).*A(4,4,:) - A(2,2,:).*(A(3,4,:).^2) - (A(2,3,:).^2.).*A(4,4,:) - A(3,3,:).*(A(2,4,:).^2) + 2*A(2,3,:).*A(2,4,:).*A(3,4,:);

Ainv(1,2,:) = -( A(1,2,:).*A(3,3,:).*A(4,4,:) - A(1,2,:).*(A(3,4,:).^2) - A(1,3,:).*A(2,3,:).*A(4,4,:) + A(2,3,:).*A(1,4,:).*A(3,4,:) +...
              A(2,4,:).*A(1,3,:).*A(3,4,:) - A(1,4,:).*A(2,4,:).*A(3,3,:) );


Ainv(1,3,:) = A(1,2,:).*A(2,3,:).*A(4,4,:) - A(1,2,:).*A(2,4,:).*A(3,4,:) - A(1,3,:).*A(2,2,:).*A(4,4,:) + ...
              A(1,4,:).*A(2,2,:).*A(3,4,:) + A(1,3,:).*(A(2,4,:).^2) - A(1,4,:).*A(2,3,:).*A(2,4,:);
          
Ainv(1,4,:) = -( A(1,2,:).*A(2,3,:).*A(3,4,:) - A(1,2,:).*A(2,4,:).*A(3,3,:) - A(1,3,:).*A(2,2,:).*A(3,4,:) + ...
              A(1,4,:).*A(2,2,:).*A(3,3,:) + A(2,3,:).*A(1,3,:).*A(2,4,:) - A(1,4,:).*(A(2,3,:).^2));


Ainv(2,2,:) = A(1,1,:).*A(3,3,:).*A(4,4,:) - A(1,1,:).*(A(3,4,:).^2) - (A(1,3,:).^2.).*A(4,4,:) + ...
              2*A(1,3,:).*A(1,4,:).*A(3,4,:) - (A(1,4,:).^2).*A(3,3,:);
          
Ainv(2,3,:) = -( A(1,1,:).*A(2,3,:).*A(4,4,:) - A(1,1,:).*A(2,4,:).*A(3,4,:) - A(1,2,:).*A(1,3,:).*A(4,4,:) + ...
              A(1,4,:).*A(1,2,:).*A(3,4,:) - (A(1,4,:).^2).*A(2,3,:) + A(1,4,:).*A(1,3,:).*A(2,4,:) );
          
Ainv(2,4,:) = A(1,1,:).*A(2,3,:).*A(3,4,:) - A(1,1,:).*A(2,4,:).*A(3,3,:) - A(1,2,:).*A(1,3,:).*A(3,4,:) + ...
              A(1,4,:).*A(1,2,:).*A(3,3,:) + (A(1,3,:).^2).*A(2,4,:) - A(1,3,:).*A(2,3,:).*A(1,4,:);
          
          
Ainv(3,3,:) = A(1,1,:).*A(2,2,:).*A(4,4,:) - A(1,1,:).*(A(2,4,:).^2) - (A(1,2,:).^2.).*A(4,4,:) + ...
              2*A(1,2,:).*A(1,4,:).*A(2,4,:) - (A(1,4,:).^2).*A(2,2,:);
          
Ainv(3,4,:) = -( A(1,1,:).*A(2,2,:).*A(3,4,:) - A(1,1,:).*A(2,4,:).*A(2,3,:) + A(1,2,:).*A(1,4,:).*A(2,3,:) + ...
              A(1,3,:).*A(1,2,:).*A(2,4,:) - (A(1,2,:).^2).*A(3,4,:) - A(1,3,:).*A(2,2,:).*A(1,4,:) );
          
     
Ainv(4,4,:) = A(1,1,:).*A(2,2,:).*A(3,3,:) - A(1,1,:).*(A(2,3,:).^2) - (A(1,2,:).^2.).*A(3,3,:) + ...
              2*A(1,2,:).*A(1,3,:).*A(2,3,:) - (A(1,3,:).^2).*A(2,2,:);
          
Ainv(2,1,:) = Ainv(1,2,:);
Ainv(3,1,:) = Ainv(1,3,:);
Ainv(3,2,:) = Ainv(2,3,:);
Ainv(4,1,:) = Ainv(1,4,:);
Ainv(4,2,:) = Ainv(2,4,:);
Ainv(4,3,:) = Ainv(3,4,:);

DetA_vector = A(1,1,:).*Ainv(1,1,:) + A(1,2,:).*Ainv(1,2,:) + A(1,3,:).*Ainv(1,3,:) + A(1,4,:).*Ainv(1,4,:);

Ainv = Ainv./(repmat(DetA_vector,[4,4,1]));

